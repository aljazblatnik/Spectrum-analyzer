char calibrationReturnMaxPower(int frequency){
    // vrnemo najvecjo moc v odvisnosti od frekvence
    int i = frequency/step;
    int j = frequency%step;
    if(j>(step/2)) i++; // ce je ostanek vecji, se priblizamo drugi vrednosti.

    if(i>6475) i = 6475;
    if(i<0) i = 0;

    return calibrationMax[i][0];
}

char calibrationReturnMaxAttenuator(int frequency){
    // vrnemo najvecjo moc v odvisnosti od frekvence
    int i = frequency/step;
    int j = frequency%step;
    if(j>(step/2)) i++; // ce je ostanek vecji, se priblizamo drugi vrednosti.

    if(i>6475) i = 6475;
    if(i<0) i = 0;

    return calibrationMax[i][1];
}

int calibrationReturnPower(int frequency, int power){
    // Vrnemo moc glede na zelje in frekvenco (max, min meje moci)
    int maxPower = calibrationReturnMaxPower(frequency);

    if(power > maxPower){
        return maxPower;
    }
    else if(power < (maxPower-30)){
        return (maxPower - 30);
    }
    else{
        return power;
    }
}

char calibrationReturnAttenuation(int frequency, int power){
    // vrnemo nastavitev slabilca glede na zeljeno moc v odvisnosti od frekvence
    char calPower = calibrationReturnMaxPower(frequency);
    char calAttenuator = calibrationReturnMaxAttenuator(frequency);

    if(power >= calPower){
        return calAttenuator;
    }
    else{
        char attenuatorSetting = calPower - power;
        attenuatorSetting = attenuatorSetting<<2; // premaknemo vrednost za 2 desno
        char returnValue = attenuatorSetting + calAttenuator;
        if(returnValue > 0b01111111){
            return 0b01111111;
        }
        else{
            return returnValue;
        }
    }
}
