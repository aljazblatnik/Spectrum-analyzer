// reduces how much is refreshed, which speeds it up!
// originally derived from Steve Evans/JCW's mod but cleaned up and
// optimized
//#define enablePartialUpdate

#ifdef enablePartialUpdate
static uint8_t xUpdateMin, xUpdateMax, yUpdateMin, yUpdateMax;
#endif



static void updateBoundingBox(uint8_t xmin, uint8_t ymin, uint8_t xmax, uint8_t ymax) {
#ifdef enablePartialUpdate
  if (xmin < xUpdateMin) xUpdateMin = xmin;
  if (xmax > xUpdateMax) xUpdateMax = xmax;
  if (ymin < yUpdateMin) yUpdateMin = ymin;
  if (ymax > yUpdateMax) yUpdateMax = ymax;
#endif
}

// the most basic function, set a single pixel
void drawPixel(int16_t x, int16_t y, uint16_t color) {
  if ((x < 0) || (x >= LCDWIDTH) || (y < 0) || (y >= LCDHEIGHT))
    return;

  int16_t t;
  switch(rotation){
    case 1:
      t = x;
      x = y;
      y =  LCDHEIGHT - 1 - t;
      break;
    case 2:
      x = LCDWIDTH - 1 - x;
      y = LCDHEIGHT - 1 - y;
      break;
    case 3:
      t = x;
      x = LCDWIDTH - 1 - y;
      y = t;
      break;
  }

  if ((x < 0) || (x >= LCDWIDTH) || (y < 0) || (y >= LCDHEIGHT))
    return;

  // x is which column
  if (color)
    pcd8544_buffer[x+ (y/8)*LCDWIDTH] |= _BV(y%8);
  else
    pcd8544_buffer[x+ (y/8)*LCDWIDTH] &= ~_BV(y%8);

  updateBoundingBox(x,y,x,y);
}


// the most basic function, get a single pixel
uint8_t getPixel(int8_t x, int8_t y) {
  if ((x < 0) || (x >= LCDWIDTH) || (y < 0) || (y >= LCDHEIGHT))
    return 0;

  return (pcd8544_buffer[x+ (y/8)*LCDWIDTH] >> (y%8)) & 0x1;
}


void start(uint8_t contrast, uint8_t bias){
    // Setup software SPI.
    // RST = PA1 - out
    // CE = PA2 - out
    // DC = PA3 . out
    // Din = PA4 - out
    // Clk = PA5 - out

    // toggle RST low to reset
    GPIOA->BSRR|=GPIO_BSRR_BR_1; //RST = 0
    delay_ms(500);
    GPIOA->BSRR|=GPIO_BSRR_BS_1; //RST = 1

    // get into the EXTENDED mode!
    command(PCD8544_FUNCTIONSET | PCD8544_EXTENDEDINSTRUCTION );

    // LCD bias select (4 is optimal?)
    command(PCD8544_SETBIAS | bias);

    // set VOP
    if (contrast > 0x7f){
        contrast = 0x7f;
    }

    command( PCD8544_SETVOP | contrast); // Experimentally determined


    // normal mode
    command(PCD8544_FUNCTIONSET);

    // Set display to Normal
    command(PCD8544_DISPLAYCONTROL | PCD8544_DISPLAYNORMAL);

    // initial display line
    // set page address
    // set column address
    // write display data

    // set up a bounding box for screen updates

    updateBoundingBox(0, 0, LCDWIDTH-1, LCDHEIGHT-1);
    // Push out pcd8544_buffer to the Display (will show the AFI logo)
    display();
}


void spiWrite(uint8_t d) {
    // Software SPI write with bit banging.
    for(unsigned char bit = 0x80; bit; bit >>= 1) {
        // clock low
        GPIOA->BSRR|=GPIO_BSRR_BR_5; // clk = 0
        if(d & bit){
            GPIOA->BSRR|=GPIO_BSRR_BS_4; // din = 1
        }
        else{
            GPIOA->BSRR|=GPIO_BSRR_BR_4; // din = 0
        }
        // clock high
        GPIOA->BSRR|=GPIO_BSRR_BS_5; // clk = 1
        delay_us(25);
      }
}

void command(uint8_t c) {
    GPIOA->BSRR|=GPIO_BSRR_BR_3; // DC = 0
    GPIOA->BSRR|=GPIO_BSRR_BR_2; // CE = 0
    spiWrite(c);
    GPIOA->BSRR|=GPIO_BSRR_BS_2; // CE = 1
    delay_us(100);
}

void data(uint8_t c) {
    GPIOA->BSRR|=GPIO_BSRR_BS_3; // DC = 1
    GPIOA->BSRR|=GPIO_BSRR_BR_2; // CE = 0
    spiWrite(c);
    GPIOA->BSRR|=GPIO_BSRR_BS_2; // CE = 1
}

void setContrast(uint8_t val) {
  if (val > 0x7f) {
    val = 0x7f;
  }
  command(PCD8544_FUNCTIONSET | PCD8544_EXTENDEDINSTRUCTION );
  command(PCD8544_SETVOP | val);
  command(PCD8544_FUNCTIONSET);
 }

void display(void) {
  uint8_t col, maxcol, p;

  for(p = 0; p < 6; p++) {
#ifdef enablePartialUpdate
    // check if this page is part of update
    if ( yUpdateMin >= ((p+1)*8) ) {
      continue;   // nope, skip it!
    }
    if (yUpdateMax < p*8) {
      break;
    }
#endif

    command(PCD8544_SETYADDR | p);


#ifdef enablePartialUpdate
    col = xUpdateMin;
    maxcol = xUpdateMax;
#else
    // start at the beginning of the row
    col = 0;
    maxcol = LCDWIDTH-1;
#endif

    command(PCD8544_SETXADDR | col);

    GPIOA->BSRR|=GPIO_BSRR_BS_3; // DC = 1
    GPIOA->BSRR|=GPIO_BSRR_BR_2; // CE = 0
    for(; col <= maxcol; col++) {
      spiWrite(pcd8544_buffer[(LCDWIDTH*p)+col]);
    }
    GPIOA->BSRR|=GPIO_BSRR_BS_2; // CE = 1

  }

  command(PCD8544_SETYADDR );  // no idea why this is necessary but it is to finish the last byte?
#ifdef enablePartialUpdate
  xUpdateMin = LCDWIDTH - 1;
  xUpdateMax = 0;
  yUpdateMin = LCDHEIGHT-1;
  yUpdateMax = 0;
#endif

}

// clear everything
void clearDisplay(void) {
  memset(pcd8544_buffer, 0, LCDWIDTH*LCDHEIGHT/8);
  updateBoundingBox(0, 0, LCDWIDTH-1, LCDHEIGHT-1);
  //cursor_y = cursor_x = 0;
}
